package com.wzl.model;

public class Detail {
    private int de_id;
    private int de_number;
    private int sh_id;
    private int or_id;

    public int getDe_id() {
        return de_id;
    }

    public void setDe_id(int de_id) {
        this.de_id = de_id;
    }

    public int getDe_number() {
        return de_number;
    }

    public void setDe_number(int de_number) {
        this.de_number = de_number;
    }

    public int getSh_id() {
        return sh_id;
    }

    public void setSh_id(int sh_id) {
        this.sh_id = sh_id;
    }

    public int getOr_id() {
        return or_id;
    }

    public void setOr_id(int or_id) {
        this.or_id = or_id;
    }
}
